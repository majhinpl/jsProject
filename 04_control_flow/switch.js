const month = "mar";

switch (month) {
  case 1:
    console.log("january");

    break;
  case 2:
    console.log("Feb");

    break;
  case "mar":
    console.log("Mar");

    break;
  case 4:
    console.log("Apr");

    break;
  case 5:
    console.log("Jun");

    break;
  case 6:
    console.log("Jul");

    break;

  default:
    console.log("Default case match");
    break;
}
